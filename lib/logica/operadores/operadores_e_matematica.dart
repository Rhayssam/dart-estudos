void main() {
  // Adição (+)
  // Subtração (-)
  // Multiplicação (*)
  // Divisão (/)
  // Resto da divisão (%)
  // Precedência (())

  int n1 = 10;
  int n2 = 5;

  int adicao = n1 + n2;
  int subtracao = n1 - n2;
  int multiplicacao = n1 * n2;
  double divisao = n1 / n2;
  int resto = n1 % n2;
  double precedencia = n1 / (n1 + n2);

  print('Adição: $adicao');
  print('Subtração: $subtracao');
  print('Multiplicação: $multiplicacao');
  print('Divisão: $divisao');
  print('Precedencia: $precedencia');
}
