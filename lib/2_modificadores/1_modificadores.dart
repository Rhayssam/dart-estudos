void main() {
  var nomeCompleto = 'Rhayssam';
  print(nomeCompleto);

  nomeCompleto = 'Rhayssam Exemplo';
  print(nomeCompleto);

  // Final - em tempo de execução
  final nomeCompletoFinal = 'Rhayssam Exemplo Final';
  print(nomeCompletoFinal);
  // Const - em tempo de compilação
  const nomeCompletoConst = 'Rhayssam Exemplo Const';
  print(nomeCompletoConst);
}
